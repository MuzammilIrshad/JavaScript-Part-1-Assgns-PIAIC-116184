function calCircumference(radius) {
    var circumference = 2 * 3.14 * radius;
    console.log("The Circumference is " + circumference);
}
function calcArea(radius) {
    var area = 3.14 * (radius ** 2);
    console.log("The area is " + area);
}
calCircumference(3);
calcArea(3);