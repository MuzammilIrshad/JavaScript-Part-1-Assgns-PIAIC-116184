var inpTime = Number(prompt("Enter Time: "));
if (inpTime >= 0000 && inpTime < 1200) {
    alert("Good Morning");
} else if (inpTime >= 1200 && inpTime < 1700) {
    alert("Good Afternoon");
} else if (inpTime >= 1700 && inpTime < 2100) {
    alert("Good Evening");
} else if (inpTime >= 2100 && inpTime <=2359) {
    alert("Good Night");
}







