var num1 = Number(prompt("Enter first number"));
var num2 = Number(prompt("Enter second number"));
var operator = prompt("Enter Operator");
if (operator == '+') {
    alert(num1 + num2);
}
else if (operator == '-') {
    alert(num1 + num2);
}
else if (operator == '*') {
    alert(num1 * num2);
}
else if (operator == '/') {
    alert(num1 / num2);
}
else if (operator == '%') {
    alert(num1 % num2);
}